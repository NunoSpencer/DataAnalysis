def CleanPoll(inF, outF):
    cleanData = []
    with open(inF, 'r') as inF, open(outF, 'w') as outF:
        for line in inF:
            cleanData = re.sub("[^0-9]", " ", line)
            #print (cleanData) #not console...file 
            outF.write(cleanData + "\n")
    print("Done cleaning poll data!")


